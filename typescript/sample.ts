//----------ユニオンタイプ（複数の型を指定できる）※多様すると型をしていできる意味がなくなるので多用はしてはいけない

let telNum: number | string;

telNum = 1244567; //number
telNum = "1234-5678"; //string

//----------------------------------------------------------------------------------

//----------型推論-----------------------------------------------------------------
let myname: any = "1234";

myname = 10;

