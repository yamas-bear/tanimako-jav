//-----------ジェネリック（その場で型をきめる）---------------------------------------

const addMrak = <T>(message: T) => {
  return "●" + message;
};

console.log(addMrak<string>("Hello World"));
//console.log(addMrak<number>("Hello World"));　型が一致していないときはエラーが発生する
